package Bean;

public class ProductBean {
	
	private String prno;
	private String prname;
	private String prstock;
	private String prprice;
	private String prcompany;
	
	public String getPrno() {
		return prno;
	}
	public void setPrno(String prno) {
		this.prno = prno;
	}
	public String getPrname() {
		return prname;
	}
	public void setPrname(String prname) {
		this.prname = prname;
	}
	public String getPrstock() {
		return prstock;
	}
	public void setPrstock(String prstock) {
		this.prstock = prstock;
	}
	public String getPrprice() {
		return prprice;
	}
	public void setPrprice(String prprice) {
		this.prprice = prprice;
	}
	public String getPrcompany() {
		return prcompany;
	}
	public void setPrcompany(String prcompany) {
		this.prcompany = prcompany;
	}
	
	
	
}
